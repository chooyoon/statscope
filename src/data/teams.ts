/**
 * Find a team by its URL-safe slug.
 */
export function getTeamBySlug(slug: string): Team | undefined {
  return Object.values(teams).find((team) => team.slug === slug);
}

/**
 * Find a team by its MLB API ID.
 */
export function getTeamById(id: number): Team | undefined {
  return teams[id];
}

/**
 * Get a team's primary and accent colors by ID.
 */
export function getTeamColor(id: number): { primary: string; accent: string; bg: string } {
  const team = teams[id];
  if (!team) {
    return { primary: '#6366f1', accent: '#818cf8', bg: '#0f0f23' };
  }
  return {
    primary: team.colorPrimary,
    accent: team.colorAccent,
    bg: team.colorBg,
  };
}
